$(function(){
    $(".alert").hide();
    $("#modetv").hide();

    var iconSelected = $("select").val();
    $("#browsericon").attr("src", "icons/" + iconSelected + ".png");
    if (iconSelected == "firefox") {
        $("#perfil").hide();
    } else {
        $("#perfil").show();
    }

    $("select").on("change", function () {
        $("#browsericon").attr("src", "icons/" + this.value + ".png");

        if (this.value == "firefox") {
            $("#perfil").hide();
        } else {
            $("#perfil").show();
        }
    });

    $("#install").click(function (e) {
        let nome = $("#namedesk").val();
        let url = $("#urldesk").val();

        if (!nome) {
            e.preventDefault();
            $("#alert-name").fadeIn();
            $("#namedesk").focus();
            setTimeout(function () {
                $("#alert-name").fadeOut();
            }, 3000);
        } else if (url == "https://" || !url) {
            e.preventDefault();
            $("#alert-url").fadeIn();
            $("#urldesk").focus();
            setTimeout(function () {
                $("#alert-url").fadeOut();
            }, 3000);
        } else {
            $("#install").submit();
        }
    });

});